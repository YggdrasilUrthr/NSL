#pragma once
#include <inttypes.h>
#include <string>

const static std::string rangen_lib_path = "../Libs/RanGen/";

// This class contains functions for generating random numbers using the RANNYU algorithm
class Random {

private:
  int m1,m2,m3,m4,l1,l2,l3,l4,n1,n2,n3,n4;

  inline bool In_Interval(double lower, double upper, double num);

protected:

public:
  // Default constructor
  Random();
  // Destructor
  ~Random();
  // Method to set the seed for the RNG
  void SetRandom(int * , int, int);
  // Method to save the seed to a file
  void SaveSeed();
  // Method to generate a random number in the range [0,1)
  double Rannyu(void);
  // Method to generate a random number in the range [min,max)
  double Rannyu(double min, double max);
  // Method to generate a random number with a Gaussian distribution
  double Gauss(double mean, double sigma);
  // Initialize random generator;
  void Init_Random_Gen(uint32_t PrimesLine = 1, std::string SeedFile = rangen_lib_path + "seed.in");
  // Generate int between min and max;
  uint32_t GenInt(uint32_t min, uint32_t max);
};

